package com.pradypods.pojo;

import org.springframework.stereotype.Service;

@Service
public class ResultCount {
    private int e;
    private int i;
    private int s;
    private int n;
    private int t;
    private int f;
    private int j;
    private int p;
    private String stri;

    public String getStri() {
        return stri;
    }

    public void setStri(String string) {
        this.stri = string;
    }

    public int getE() {
        return e;
    }

    public void setE(int e) {
        this.e = e;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getT() {
        return t;
    }

    public void setT(int t) {
        this.t = t;
    }

    public int getF() {
        return f;
    }

    public void setF(int f) {
        this.f = f;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }
}
